function deterministicObjectId(...parts) {
  const source = [SEED_NAMESPACE, ...parts.map(String)].join(':');
  const hex = crypto.createHash('md5').update(source).digest('hex').slice(0, 24);
  return new ObjectId(hex);
}

function normalizeObjectId(value) {
  if (!value) return null;
  if (value instanceof ObjectId) return value;
  return new ObjectId(String(value));
}

function pad(value, width = 2) {
  return String(value).padStart(width, '0');
}

function daysAgo(days) {
  const date = new Date();
  date.setUTCDate(date.getUTCDate() - days);
  date.setUTCHours(3 + (days % 11), (days * 7) % 60, (days * 13) % 60, 0);
  return date;
}

function pick(array, index) {
  return array[index % array.length];
}

function cleanText(value) {
  return String(value || '').trim();
}

function productName(product) {
  return cleanText(product.productName || product.product_name || product.name) || `Sản phẩm ${String(product._id).slice(-6)}`;
}

function productPrice(product) {
  const candidates = [
    product.price,
    product.salePriceAmount,
    product.sale_price_amount,
    product.finalPrice,
    product.final_price,
    product.compareAtPrice,
  ];
  for (const value of candidates) {
    const parsed = Number(value);
    if (Number.isFinite(parsed) && parsed >= 0) return Math.round(parsed);
  }
  return 199000;
}

function variantName(variant) {
  return cleanText(variant.variantName || variant.variant_name || variant.sku) || `Biến thể ${String(variant._id).slice(-6)}`;
}

function variantSku(variant, productIndex, reviewIndex) {
  return cleanText(variant.sku || variant.SKU) || `KNL-REVIEW-${pad(productIndex + 1, 4)}-${pad(reviewIndex + 1, 2)}`;
}

function variantProductId(variant) {
  return normalizeObjectId(variant.productId || variant.product_id || variant.product || null);
}

function detectProductType(product) {
  const haystack = [
    productName(product),
    product.shortDescription,
    product.longDescription,
    product.productCode,
    product.slug,
  ].map((value) => cleanText(value).toLowerCase()).join(' ');

  const hasAny = (...keywords) => keywords.some((keyword) => haystack.includes(keyword));

  if (hasAny('sữa rửa mặt', 'cleanser', 'cleansing foam', 'face wash', 'perfect whip')) return 'cleanser';
  if (hasAny('tẩy trang', 'micellar', 'cleansing water', 'cleansing oil', 'makeup remover')) return 'micellar';
  if (hasAny('chống nắng', 'sunscreen', 'sun cream', 'uv ', 'spf')) return 'sunscreen';
  if (hasAny('serum', 'essence', 'ampoule', 'tinh chất')) return 'serum';
  if (hasAny('kem dưỡng', 'moisturizer', 'cream', 'gel cream', 'sleeping mask')) return 'moisturizer';
  if (hasAny('toner', 'lotion', 'nước cân bằng')) return 'toner';
  if (hasAny('son', 'lip tint', 'lipstick', 'lip glow', 'lip balm', 'tint')) return 'lip';
  if (hasAny('kem nền', 'foundation', 'cushion', 'concealer', 'che khuyết điểm', 'primer')) return 'base';
  if (hasAny('mascara', 'eyeliner', 'kẻ mắt')) return 'mascara';
  if (hasAny('má hồng', 'blush', 'phấn mắt', 'eyeshadow', 'highlighter', 'bronzer', 'phấn phủ', 'powder')) return 'colorMakeup';
  return 'generic';
}

function buildPositiveReview(product, reviewIndex, globalIndex) {
  const type = detectProductType(product);
  const library = REVIEW_LIBRARY[type] || REVIEW_LIBRARY.generic;
  const [title, coreContent] = pick(library, reviewIndex);
  const context = pick(USAGE_CONTEXTS, globalIndex);
  const skin = pick(SKIN_CONTEXTS, globalIndex + reviewIndex);
  const name = productName(product);

  return {
    title: title || pick(GENERIC_TITLES, globalIndex),
    content: `${coreContent} Mình có ${skin}, ${context} và thấy ${name} khá dễ dùng. Hàng nhận đúng mô tả, bao bì nguyên vẹn.`,
  };
}

function buildOrderNumber(productIndex, reviewIndex) {
  return `KRV${new Date().getUTCFullYear()}${pad(productIndex + 1, 4)}${pad(reviewIndex + 1, 2)}`;
}

function customerDisplayName(customer, fallbackIndex) {
  return cleanText(customer.full_name || customer.fullName || customer.name) || `Khách hàng Kanila ${pad(fallbackIndex + 1, 3)}`;
}

function customerPhone(customer, fallbackIndex) {
  const existing = cleanText(customer.phone || customer.phone_number);
  if (existing) return existing;
  return `09${String(10000000 + ((fallbackIndex + 1) * 7919)).slice(-8)}`;
}

async function connectMongo() {
  const uri = process.env.MONGODB_URI
    || process.env.MONGO_URI
    || process.env.MONGODB_URL
    || process.env.DATABASE_URL;

  if (!uri) {
    throw new Error('Missing MongoDB connection string. Set MONGODB_URI or MONGO_URI in .env');
  }

  const options = process.env.MONGODB_DB_NAME
    ? { dbName: process.env.MONGODB_DB_NAME }
    : undefined;

  await mongoose.connect(uri, options);
  console.log(`Connected to MongoDB database: ${mongoose.connection.name}`);
}

async function getCollectionNames(db) {
  const rows = await db.listCollections({}, { nameOnly: true }).toArray();
  return new Set(rows.map((row) => row.name));
}

async function loadContext(db) {
  const products = await db.collection(COLLECTION.products)
    .find({})
    .sort({ _id: 1 })
    .toArray();

  if (!products.length) {
    throw new Error('No products found. Run dataproduct_copy.js before datareview.js.');
  }

  const variants = await db.collection(COLLECTION.variants)
    .find({})
    .sort({ _id: 1 })
    .toArray();

  if (!variants.length) {
    throw new Error('No product_variants found. Run dataproduct_copy.js before datareview.js.');
  }

  const customers = await db.collection(COLLECTION.customers)
    .find({})
    .sort({ _id: 1 })
    .toArray();

  if (customers.length < REVIEWS_PER_PRODUCT) {
    throw new Error(
      `Only ${customers.length} customer_profiles found. `
      + `At least ${REVIEWS_PER_PRODUCT} customers are required so each product can receive distinct reviews. `
      + 'Run dataremaining.js first.',
    );
  }

  const customerAddresses = await db.collection(COLLECTION.customerAddresses)
    .find({})
    .sort({ _id: 1 })
    .toArray();

  const variantsByProduct = new Map();
  for (const variant of variants) {
    const productId = variantProductId(variant);
    if (!productId) continue;
    const key = String(productId);
    if (!variantsByProduct.has(key)) variantsByProduct.set(key, []);
    variantsByProduct.get(key).push(variant);
  }

  const addressByCustomer = new Map();
  for (const address of customerAddresses) {
    if (!address.customer_id) continue;
    const key = String(address.customer_id);
    if (!addressByCustomer.has(key) || address.is_default_shipping) {
      addressByCustomer.set(key, address);
    }
  }

  const productsWithoutVariants = products.filter((product) => !variantsByProduct.has(String(product._id)));
  if (productsWithoutVariants.length) {
    const names = productsWithoutVariants.slice(0, 5).map(productName).join(', ');
    throw new Error(
      `${productsWithoutVariants.length} products do not have mapped variants. `
      + `Examples: ${names}. Check product_variants.productId/product_id before seeding reviews.`,
    );
  }

  return {
    products,
    customers,
    variantsByProduct,
    addressByCustomer,
  };
}

function buildSeedDocuments(ctx) {
  const orders = [];
  const orderItems = [];
  const orderTotals = [];
  const orderAddresses = [];
  const orderStatusHistory = [];
  const reviews = [];

  ctx.products.forEach((product, productIndex) => {
    const productVariants = ctx.variantsByProduct.get(String(product._id));

    for (let reviewIndex = 0; reviewIndex < REVIEWS_PER_PRODUCT; reviewIndex += 1) {
      const globalIndex = productIndex * REVIEWS_PER_PRODUCT + reviewIndex;
      const customer = ctx.customers[(globalIndex + productIndex) % ctx.customers.length];
      const variant = productVariants[reviewIndex % productVariants.length];

      const orderId = deterministicObjectId('order', product._id, reviewIndex);
      const orderItemId = deterministicObjectId('order-item', product._id, reviewIndex);
      const orderTotalId = deterministicObjectId('order-total', product._id, reviewIndex);
      const orderAddressId = deterministicObjectId('order-address', product._id, reviewIndex);
      const orderStatusHistoryId = deterministicObjectId('order-status-history', product._id, reviewIndex);
      const reviewId = deterministicObjectId('review', product._id, reviewIndex);

      const placedAt = daysAgo(150 - ((globalIndex * 3) % 120));
      const confirmedAt = new Date(placedAt.getTime() + 4 * 60 * 60 * 1000);
      const completedAt = new Date(placedAt.getTime() + (3 + (reviewIndex % 4)) * 24 * 60 * 60 * 1000);
      const reviewedAt = new Date(completedAt.getTime() + (2 + (reviewIndex % 12)) * 24 * 60 * 60 * 1000);

      const listPrice = productPrice(product);
      const discountRate = reviewIndex % 4 === 0 ? 0.1 : reviewIndex % 5 === 0 ? 0.05 : 0;
      const finalPrice = Math.max(0, Math.round((listPrice * (1 - discountRate)) / 1000) * 1000);
      const discountAmount = listPrice - finalPrice;
      const shippingFee = reviewIndex % 3 === 0 ? 0 : 25000;
      const grandTotal = finalPrice + shippingFee;
      const orderNumber = buildOrderNumber(productIndex, reviewIndex);
      const rating = RATING_PATTERN[reviewIndex % RATING_PATTERN.length];
      const reviewText = buildPositiveReview(product, reviewIndex, globalIndex);
      const address = ctx.addressByCustomer.get(String(customer._id));

      orders.push({
        _id: orderId,
        owner_type: 'customer',
        guest_session_id: null,
        guest_email: '',
        guest_phone: '',
        guest_full_name: '',
        order_number: orderNumber,
        customer_id: customer._id,
        checkout_session_id: null,
        currency_code: 'VND',
        order_status: 'completed',
        payment_status: 'paid',
        fulfillment_status: 'delivered',
        customer_note: 'Đơn hàng mock phục vụ dữ liệu đánh giá sản phẩm Kanila.',
        placed_at: placedAt,
        confirmed_at: confirmedAt,
        cancelled_at: null,
        completed_at: completedAt,
        cancellation_reason: '',
        created_at: placedAt,
        updated_at: completedAt,
        __v: 0,
      });

      orderItems.push({
        _id: orderItemId,
        order_id: orderId,
        product_id: product._id,
        variant_id: variant._id,
        sku_snapshot: variantSku(variant, productIndex, reviewIndex),
        product_name_snapshot: productName(product),
        variant_name_snapshot: variantName(variant),
        quantity: 1,
        unit_list_price_amount: listPrice,
        unit_sale_price_amount: finalPrice,
        unit_final_price_amount: finalPrice,
        line_subtotal_amount: listPrice,
        line_discount_amount: discountAmount,
        line_total_amount: finalPrice,
        currency_code: 'VND',
        created_at: placedAt,
        updated_at: completedAt,
        __v: 0,
      });

      orderTotals.push({
        _id: orderTotalId,
        order_id: orderId,
        subtotal_amount: listPrice,
        item_discount_amount: discountAmount,
        order_discount_amount: 0,
        shipping_fee_amount: shippingFee,
        tax_amount: 0,
        grand_total_amount: grandTotal,
        refunded_amount: 0,
        currency_code: 'VND',
        created_at: placedAt,
        updated_at: completedAt,
        __v: 0,
      });

      orderAddresses.push({
        _id: orderAddressId,
        order_id: orderId,
        address_type: 'shipping',
        recipient_name: address?.recipient_name || customerDisplayName(customer, globalIndex),
        phone: address?.phone || customerPhone(customer, globalIndex),
        address_line_1: address?.address_line_1 || `${20 + (globalIndex % 180)} đường Kanila Beauty`,
        address_line_2: address?.address_line_2 || '',
        ward: address?.ward || 'Phường Bến Nghé',
        district: address?.district || 'Quận 1',
        city: address?.city || 'TP. Hồ Chí Minh',
        country_code: address?.country_code || 'VN',
        postal_code: address?.postal_code || '700000',
        created_at: placedAt,
        __v: 0,
      });

      orderStatusHistory.push({
        _id: orderStatusHistoryId,
        order_id: orderId,
        old_order_status: 'processing',
        new_order_status: 'completed',
        old_payment_status: 'authorized',
        new_payment_status: 'paid',
        old_fulfillment_status: 'shipped',
        new_fulfillment_status: 'delivered',
        changed_by_account_id: null,
        change_reason: 'Đơn hàng đã giao thành công và đủ điều kiện đánh giá.',
        changed_at: completedAt,
        __v: 0,
      });

      reviews.push({
        _id: reviewId,
        customer_id: customer._id,
        orderItemId,
        productId: product._id,
        variantId: variant._id,
        rating,
        reviewTitle: reviewText.title,
        reviewContent: reviewText.content,
        reviewStatus: 'visible',
        helpfulCount: (productIndex * 7 + reviewIndex * 3) % 36,
        verifiedPurchaseFlag: true,
        createdAt: reviewedAt,
        updatedAt: reviewedAt,
        __v: 0,
      });
    }
  });

  return {
    orders,
    orderItems,
    orderTotals,
    orderAddresses,
    orderStatusHistory,
    reviews,
  };
}

async function resetGeneratedRows(db, docs) {
  if (!SHOULD_RESET) return;

  console.log('Reset mode: deleting rows generated by datareview.js...');
  const resetPlan = [
    [COLLECTION.reviews, docs.reviews],
    [COLLECTION.orderStatusHistory, docs.orderStatusHistory],
    [COLLECTION.orderAddresses, docs.orderAddresses],
    [COLLECTION.orderTotals, docs.orderTotals],
    [COLLECTION.orderItems, docs.orderItems],
    [COLLECTION.orders, docs.orders],
  ];

  for (const [collectionName, rows] of resetPlan) {
    const ids = rows.map((row) => row._id);
    const result = await db.collection(collectionName).deleteMany({ _id: { $in: ids } });
    console.log(`  ${collectionName.padEnd(28)} deleted ${String(result.deletedCount).padStart(5)}`);
  }
}

async function replaceManyById(db, collectionName, docs) {
  if (!docs.length) return;

  const operations = docs.map((doc) => ({
    replaceOne: {
      filter: { _id: doc._id },
      replacement: doc,
      upsert: true,
    },
  }));

  const result = await db.collection(collectionName).bulkWrite(operations, { ordered: false });
  console.log(
    `${collectionName.padEnd(28)} prepared ${String(docs.length).padStart(5)}`
    + ` | upserted ${String(result.upsertedCount || 0).padStart(5)}`
    + ` | modified ${String(result.modifiedCount || 0).padStart(5)}`,
  );
}

async function synchronizeReviewSummaries(db, products, collectionNames) {
  let summaryCollection = 'review_summary';
  if (collectionNames.has('review_summary')) summaryCollection = 'review_summary';
  else if (collectionNames.has('review_summaries')) summaryCollection = 'review_summaries';

  const productIds = products.map((product) => product._id);
  const aggregateRows = await db.collection(COLLECTION.reviews).aggregate([
    {
      $match: {
        productId: { $in: productIds },
        reviewStatus: 'visible',
      },
    },
    {
      $group: {
        _id: '$productId',
        reviewCount: { $sum: 1 },
        averageRating: { $avg: '$rating' },
        rating1Count: { $sum: { $cond: [{ $eq: ['$rating', 1] }, 1, 0] } },
        rating2Count: { $sum: { $cond: [{ $eq: ['$rating', 2] }, 1, 0] } },
        rating3Count: { $sum: { $cond: [{ $eq: ['$rating', 3] }, 1, 0] } },
        rating4Count: { $sum: { $cond: [{ $eq: ['$rating', 4] }, 1, 0] } },
        rating5Count: { $sum: { $cond: [{ $eq: ['$rating', 5] }, 1, 0] } },
      },
    },
  ]).toArray();

  const byProduct = new Map(aggregateRows.map((row) => [String(row._id), row]));
  const now = new Date();
  const operations = products.map((product) => {
    const aggregate = byProduct.get(String(product._id)) || {
      reviewCount: 0,
      averageRating: 0,
      rating1Count: 0,
      rating2Count: 0,
      rating3Count: 0,
      rating4Count: 0,
      rating5Count: 0,
    };

    return {
      updateOne: {
        filter: { productId: product._id },
        update: {
          $set: {
            productId: product._id,
            reviewCount: aggregate.reviewCount,
            averageRating: Number((aggregate.averageRating || 0).toFixed(2)),
            rating1Count: aggregate.rating1Count,
            rating2Count: aggregate.rating2Count,
            rating3Count: aggregate.rating3Count,
            rating4Count: aggregate.rating4Count,
            rating5Count: aggregate.rating5Count,
            updatedAt: now,
            __v: 0,
          },
          $setOnInsert: {
            _id: deterministicObjectId('review-summary', product._id),
            createdAt: now,
          },
        },
        upsert: true,
      },
    };
  });

  const result = await db.collection(summaryCollection).bulkWrite(operations, { ordered: false });
  console.log(
    `${summaryCollection.padEnd(28)} synchronized ${String(products.length).padStart(5)}`
    + ` | upserted ${String(result.upsertedCount || 0).padStart(5)}`
    + ` | modified ${String(result.modifiedCount || 0).padStart(5)}`,
  );
}

async function verify(db, products) {
  const generatedReviewIds = [];
  for (const product of products) {
    for (let reviewIndex = 0; reviewIndex < REVIEWS_PER_PRODUCT; reviewIndex += 1) {
      generatedReviewIds.push(deterministicObjectId('review', product._id, reviewIndex));
    }
  }

  const generatedReviewCount = await db.collection(COLLECTION.reviews).countDocuments({
    _id: { $in: generatedReviewIds },
  });

  const perProduct = await db.collection(COLLECTION.reviews).aggregate([
    {
      $match: {
        _id: { $in: generatedReviewIds },
        reviewStatus: 'visible',
        rating: { $in: [4, 5] },
        verifiedPurchaseFlag: true,
      },
    },
    { $group: { _id: '$productId', count: { $sum: 1 }, averageRating: { $avg: '$rating' } } },
    { $sort: { count: 1 } },
  ]).toArray();

  const invalidProducts = perProduct.filter((row) => row.count !== REVIEWS_PER_PRODUCT);
  if (generatedReviewCount !== products.length * REVIEWS_PER_PRODUCT || invalidProducts.length) {
    throw new Error(
      `Verification failed. Expected ${products.length * REVIEWS_PER_PRODUCT} generated reviews, `
      + `found ${generatedReviewCount}; products with invalid review counts: ${invalidProducts.length}.`,
    );
  }

  const minAverage = Math.min(...perProduct.map((row) => row.averageRating));
  const maxAverage = Math.max(...perProduct.map((row) => row.averageRating));

  console.log('\nVerification passed:');
  console.log(`  Products covered       : ${products.length}`);
  console.log(`  Reviews per product    : ${REVIEWS_PER_PRODUCT}`);
  console.log(`  Generated reviews      : ${generatedReviewCount}`);
  console.log(`  Ratings                : only 4-5 stars`);
  console.log(`  Verified purchases     : 100%`);
  console.log(`  Average rating range   : ${minAverage.toFixed(2)} - ${maxAverage.toFixed(2)}`);
}

async function run() {
  await connectMongo();
  const db = mongoose.connection.db;
  const collectionNames = await getCollectionNames(db);
  const context = await loadContext(db);
  const docs = buildSeedDocuments(context);

  console.log(`Products found: ${context.products.length}`);
  console.log(`Customers found: ${context.customers.length}`);
  console.log(`Reviews per product: ${REVIEWS_PER_PRODUCT}`);
  console.log(`Total review-linked orders to prepare: ${docs.orders.length}\n`);

  await resetGeneratedRows(db, docs);

  const seedPlan = [
    [COLLECTION.orders, docs.orders],
    [COLLECTION.orderItems, docs.orderItems],
    [COLLECTION.orderTotals, docs.orderTotals],
    [COLLECTION.orderAddresses, docs.orderAddresses],
    [COLLECTION.orderStatusHistory, docs.orderStatusHistory],
    [COLLECTION.reviews, docs.reviews],
  ];

  for (const [collectionName, rows] of seedPlan) {
    await replaceManyById(db, collectionName, rows);
  }

  await synchronizeReviewSummaries(db, context.products, collectionNames);
  await verify(db, context.products);

  console.log('\nKanila review seed completed successfully.');
}

run()
  .then(async () => {
    await mongoose.disconnect();
    process.exit(0);
  })
  .catch(async (error) => {
    console.error('\nKanila review seed failed:', error);
    try {
      await mongoose.disconnect();
    } catch (_) {
      // Ignore disconnect errors while handling the original failure.
    }
    process.exit(1);
  });
