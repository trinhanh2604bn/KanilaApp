/**
 * datareview.js
 * Kanila Beauty Commerce - seed 10 positive reviews (4-5 stars) for every product.
 *
 * Designed to run after:
 *   1) dataproduct_copy.js
 *   2) dataremaining.js
 *
 * The script reads the existing products, product variants and customers, then creates:
 * - 1 completed mock order for each review
 * - 1 matching order item for each review
 * - 1 order total, order address and completed status history for each order
 * - 10 visible, verified-purchase reviews for every product
 * - review_summary/review_summaries aggregates synchronized from visible reviews
 *
 * Review documents use the current Review schema exactly:
 *   customer_id, orderItemId, productId, variantId, rating,
 *   reviewTitle, reviewContent, reviewStatus, helpfulCount,
 *   verifiedPurchaseFlag, createdAt, updatedAt, __v
 *
 * Usage from backend root:
 *   npm install dotenv mongoose
 *   node datareview.js
 *
 * Optional:
 *   node datareview.js --reset
 *     Deletes only deterministic rows generated by this script, then inserts them again.
 *
 *   REVIEWS_PER_PRODUCT=10 node datareview.js
 *     Changes the number of reviews generated for each product.
 *
 * Requirements:
 *   .env must contain one of: MONGODB_URI, MONGO_URI, MONGODB_URL, DATABASE_URL
 */

